    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        // TODO add your handling code here:
        
        // TODO add your handling code here:
        try{
            if(action.equalsIgnoreCase("Tambah")){
                EmptyInputException();
                if(jrButtonFull.isSelected() == true) {
                    f = new Full(txtnoProduk.getText()
                            ,txtnamaProduk.getText()
                            ,Integer.parseInt(txtharga.getText())
                            ,Integer.parseInt(txtjumlah.getText())
                            ,Integer.parseInt(txtTotalFull.getText()));
                    fuco.insertDataFull(f);
                    JOptionPane.showMessageDialog(this, "Data berhasil ditambah!");
                } else if(jrButtonDeposit.isSelected() == true) {
                    d = new Deposit(txtnoProduk.getText()
                            ,txtnamaProduk.getText()
                            ,Integer.parseInt(txtharga.getText())
                            ,Integer.parseInt(txtjumlah.getText())
                            ,Integer.parseInt(txtTotalDeposit.getText()));
                    deco.insertDataDeposit(d);
                    JOptionPane.showMessageDialog(this, "Data berhasil ditambah!");
                }
                clearTxt();
                ShowTableFull();
                ShowTableDeposit();
            } 
            ShowTable();
        } catch (EmptyInputEx e) {
            JOptionPane.showMessageDialog(this,e.message());
        } catch (NumberFormatException e2) {
            JOptionPane.showMessageDialog(this, e2.getMessage());
        }
        
    }//GEN-LAST:event_btnDoneActionPerformed

