    public void EmptyInputException() throws EmptyInputEx{
        if(txtnoProduk.getText().equalsIgnoreCase("") 
           || txtnamaProduk.getText().equalsIgnoreCase("")
           || txtharga.getText().equalsIgnoreCase("")
           || txtjumlah.getText().equalsIgnoreCase("")) {
            if(jrButtonFull.isSelected()==true && txtTotalFull.getText().equalsIgnoreCase(""))
                throw new EmptyInputEx();
        }
    }
    
